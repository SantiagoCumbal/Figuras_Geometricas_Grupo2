package CLASES_FIGURAS;

public class TRES_D {
    int ncaras;
    double aristas;
    public TRES_D() {}
    public TRES_D(int ncaras, double aristas) {
        this.ncaras = ncaras;
        this.aristas = aristas;
    }

    public int getNcaras() {
        return ncaras;
    }

    public void setNcaras(int ncaras) {
        this.ncaras = ncaras;
    }

    public double getAristas() {
        return aristas;
    }

    public void setAristas(double aristas) {
        this.aristas = aristas;
    }
}
